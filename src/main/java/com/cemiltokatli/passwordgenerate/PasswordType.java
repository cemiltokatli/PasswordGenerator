package com.cemiltokatli.passwordgenerate;

/**
 * Password Types
 */
public enum PasswordType {
    /**
     * Letters, numbers and symbols
     */
    ALL,
    /**
     * Only letters
     */
    ALPHA,
    /**
     * Only numbers
     */
    NUMERIC,
    /**
     * Letters and numbers
     */
    ALPHANUMERIC,
    /**
     * Only symbols
     */
    SYMBOLS
}
